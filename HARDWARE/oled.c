void OLED_DisplayTurn(u8 i)
{
	if(i==0)
		{
			OLED_WR_Byte(0xC8,OLED_CMD, false);//正常显示
			OLED_WR_Byte(0xA1,OLED_CMD, false);
		}
	if(i==1)
		{
			OLED_WR_Byte(0xC0,OLED_CMD, false);//反转显示
			OLED_WR_Byte(0xA0,OLED_CMD, false);
		}
}

